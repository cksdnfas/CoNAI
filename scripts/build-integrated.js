#!/usr/bin/env node

/**
 * ComfyUI Image Manager - Integrated Build Script
 * Frontend + Backend 통합 빌드
 */

const fs = require('fs-extra');
const path = require('path');
const { execSync } = require('child_process');

const ROOT_DIR = path.resolve(__dirname, '..');
const SHARED_DIR = path.join(ROOT_DIR, 'shared');
const FRONTEND_DIR = path.join(ROOT_DIR, 'frontend');
const BACKEND_DIR = path.join(ROOT_DIR, 'backend');
const SHARED_DIST = path.join(SHARED_DIR, 'dist');
const FRONTEND_DIST = path.join(FRONTEND_DIR, 'dist');
const BACKEND_DIST = path.join(BACKEND_DIR, 'dist');
const INTEGRATED_DIST = path.join(BACKEND_DIST, 'frontend');

console.log('🚀 ComfyUI Image Manager - Integrated Build\n');

// Step 1: Clean previous builds
console.log('🧹 Cleaning previous builds...');
try {
  fs.removeSync(SHARED_DIST);
  fs.removeSync(FRONTEND_DIST);
  fs.removeSync(BACKEND_DIST);
  console.log('✅ Cleaned successfully\n');
} catch (error) {
  console.warn('⚠️  Clean failed (might be first build):', error.message, '\n');
}

// Step 2: Build Shared package
console.log('📦 Building Shared package...');
try {
  execSync('npm run build', {
    cwd: SHARED_DIR,
    stdio: 'inherit'
  });
  console.log('✅ Shared package build completed\n');
} catch (error) {
  console.error('❌ Shared package build failed:', error.message);
  process.exit(1);
}

// Step 3: Build Frontend
console.log('🎨 Building Frontend...');
try {
  execSync('npm run build', {
    cwd: FRONTEND_DIR,
    stdio: 'inherit'
  });
  console.log('✅ Frontend build completed\n');
} catch (error) {
  console.error('❌ Frontend build failed:', error.message);
  process.exit(1);
}

// Step 4: Build Backend
console.log('🔧 Building Backend...');
try {
  execSync('npm run build', {
    cwd: BACKEND_DIR,
    stdio: 'inherit'
  });
  console.log('✅ Backend build completed\n');
} catch (error) {
  console.error('❌ Backend build failed:', error.message);
  process.exit(1);
}

// Step 5: Copy Frontend to Backend dist
console.log('📦 Integrating Frontend into Backend...');
try {
  if (!fs.existsSync(FRONTEND_DIST)) {
    throw new Error('Frontend dist not found');
  }

  fs.ensureDirSync(INTEGRATED_DIST);
  fs.copySync(FRONTEND_DIST, INTEGRATED_DIST, {
    overwrite: true,
    dereference: true
  });

  const fileCount = fs.readdirSync(INTEGRATED_DIST).length;
  console.log(`✅ Copied ${fileCount} files/folders to backend/dist/frontend\n`);
} catch (error) {
  console.error('❌ Integration failed:', error.message);
  process.exit(1);
}

// Step 6: Create environment template
console.log('⚙️  Creating environment template...');
try {
  const envTemplate = `# ComfyUI Image Manager Configuration
# Generated by integrated build

# Server Configuration
PORT=1566
HOST=0.0.0.0
BACKEND_PROTOCOL=http

# Public Access URLs (Optional - for external access)
# PUBLIC_BASE_URL=http://your-external-ip:1566
# BACKEND_HOST=your-external-ip

# Runtime Paths (Optional - defaults to current directory)
# RUNTIME_BASE_PATH=./data
# FRONTEND_DIST_PATH=./dist/frontend

# Development
NODE_ENV=production
`;

  fs.writeFileSync(
    path.join(BACKEND_DIST, '.env.example'),
    envTemplate,
    'utf8'
  );
  console.log('✅ Environment template created\n');
} catch (error) {
  console.warn('⚠️  Environment template creation failed:', error.message, '\n');
}

// Step 7: Display build summary
console.log('📊 Build Summary:');
console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');

const getDirectorySize = (dirPath) => {
  let size = 0;
  const files = fs.readdirSync(dirPath);

  for (const file of files) {
    const filePath = path.join(dirPath, file);
    const stats = fs.statSync(filePath);

    if (stats.isDirectory()) {
      size += getDirectorySize(filePath);
    } else {
      size += stats.size;
    }
  }

  return size;
};

const formatSize = (bytes) => {
  if (bytes < 1024) return bytes + ' B';
  if (bytes < 1024 * 1024) return (bytes / 1024).toFixed(2) + ' KB';
  return (bytes / (1024 * 1024)).toFixed(2) + ' MB';
};

try {
  const backendSize = getDirectorySize(BACKEND_DIST);
  const frontendSize = getDirectorySize(INTEGRATED_DIST);

  console.log(`📁 Backend dist:  ${formatSize(backendSize)}`);
  console.log(`🎨 Frontend dist: ${formatSize(frontendSize)}`);
  console.log(`📦 Total size:    ${formatSize(backendSize)}`);
} catch (error) {
  console.warn('⚠️  Could not calculate size:', error.message);
}

console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
console.log('\n✨ Integrated build completed successfully!');
console.log('\n🚀 Next steps:');
console.log('   1. Test: cd backend && node dist/index.js');
console.log('   2. Build SEA: npm run build:sea');
console.log('   3. Open: http://localhost:1566\n');
